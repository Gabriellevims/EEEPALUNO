    private void cadastraCursoView() {
        Curso objCurso = new Curso(WIDTH, title, title);
        objCurso.setNomeCurso(JtfNomeCurso.getText());
        objCurso.setStatusCurso((String) CbxStatusCurso.getSelectedItem());
        if (JtfNomeCurso.getText()
                .isEmpty()) {
            JOptionPane.showMessageDialog(null, "Os campos n√£o podem retonar vazios");
        } else {
            ControleCurso objControleCurso = new ControleCurso();
            objControleCurso.cadastrarCurso(objCurso);
            JOptionPane.showMessageDialog(null, "Curso cadastrado com Sucesso!");
        }
    }

    private void listarCursoView() {
        try {
            ControleCurso objControlecurso = new ControleCurso();
            // usar a tabela para exibir os valores
            //getModel para montar o modelo de tabela 
            DefaultTableModel model = (DefaultTableModel) JTableCurso.getModel();
            model.setNumRows(0);
            ArrayList<Curso> lista = objControlecurso.consultarCurso();
            // funciona para cria lisra de acordo com a nessecidade com o listasize 
            for (int num = 0; num < lista.size(); num++) {
                // adicionar um linha para cada informa√ß√£o
                model.addRow(new Object[]{
                    lista.get(num).getIdCurso(),
                    lista.get(num).getNomeCurso(),
                    lista.get(num).getStatusCurso(),});
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Lista curso view frmcurso" + erro);
        }
    }

    private void carregarCampos() {
        int setar = JTableCurso.getSelectedRow();

        JtfIdCurso.setText(JTableCurso.getModel().getValueAt(setar, 0).toString());
        JtfNomeCurso.setText(JTableCurso.getModel().getValueAt(setar, 1).toString());
        CbxStatusCurso.setSelectedItem(JTableCurso.getModel().getValueAt(setar, 2).toString());
    }

    private void alterarCurso() {
        if (JtfNomeCurso.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Os campos n√£o podem retorna vazios");
        } else {
            int idCurso = Integer.parseInt(JtfIdCurso.getText());
            Curso objCurso = new Curso(idCurso, JtfNomeCurso.getText(), CbxStatusCurso.getSelectedItem().toString());
            ControleCurso objControleCurso = new ControleCurso();
            objControleCurso.alterarCurso(objCurso);
            JOptionPane.showConfirmDialog(null, "Curso alterado com sucesso!");
        }
    }

    private void limparCampos() {
        JtfIdCurso.setText("");
        JtfNomeCurso.setText("");
        CbxStatusCurso.setSelectedIndex(0);
        JtfNomeCurso.requestFocus();

    }

    private void excluirCursoView() {
        if (JtfIdCurso.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "por favor selecione um curso para excluir");
        } else {
            int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o cursos", "confirma√ß√£o", JOptionPane.YES_NO_OPTION);
            if (confirmacao == JOptionPane.YES_OPTION) {
                try {
                    Curso objCurso = new Curso(WIDTH, title, title);
                    objCurso.setIdCurso(Integer.parseInt(JtfIdCurso.getText()));
                    ControleCurso objControleCurso = new ControleCurso();
                    objControleCurso.excluirCurso(objCurso);
                    JOptionPane.showConfirmDialog(null, "curso excluido com sucesso");
                    limparCampos();
                } catch (NumberFormatException erro) {
                    JOptionPane.showConfirmDialog(null, "Erro; ID curso inv√°lido. Por favor, verifique os dados.");
                }
            }
        }
    }
